package day11.Collection工具类.sort方法;

public class Person12 {
    String name;
    int age;

    public Person12(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
